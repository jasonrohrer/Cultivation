Cultivation is a game about a community of gardeners growing food for themselves in a shared space.<BR>
<BR>  

Cultivation is quite different from most other games.  It is a social simulation, and the primary form of conflict is over land and plant resources---there is no shooting, but there are plenty of angry looks.  It is also an evolution simulation.  Within the world of Cultivation, you can explore a virtually infinite spectrum of different plant and gardener varieties.<BR>
<BR>
All of the graphics, sounds, melodies, and other content in Cultivation are 100% procedurally generated at playtime.  In other words, there are no hand-painted texture maps---instead, each object has a uniquely "grown" appearance.  Every time you play, Cultivation generates fresh visuals, music, and behaviors.  You can read more about what I was trying to do with Cultivation in <a href="http://www.northcountrynotes.org/jason-rohrer/arthouseGames/seedBlogs.php?action=display_post&post_id=jcr13_1169054799_0&show_author=1&show_date=1">this essay</a><br>
