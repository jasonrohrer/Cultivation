<H2>Requirements</H2>

Required:
<UL>
<LI>OpenGL</LI>
<LI>Mouse</LI>
</UL>

Recommended:
<UL>
<LI>3D card that can render OpenGL</LI>
<LI>400 MHz or faster processor</LI>
<LI>Stereo headphones</LI>
</UL>

Notes:<BR>
Development and testing took place on a 250 MHz PPC computer <I>without</I> a 3D accelerator card (in other words, using software OpenGL rendering).  
Cultivation is actually playable on a meager system like this, though it runs a lot smoother on a faster computer with a 3D card.<BR>
<BR>
If you have an older computer, you may be able to improve Cultivation's performance by disabling some of the more complicated graphical features.  Open the included file <b>features.txt</b> with a text editor (changing a "1" to a "0" will turn a given feature off).


